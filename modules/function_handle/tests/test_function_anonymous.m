F = @() (3);
R = func2str(F);
REF = '@()3';
assert_isequal(R, REF);
%=============================================================================
F = @(x) (3);
R = func2str(F);
REF = '@(x)3';
assert_isequal(R, REF);
%=============================================================================
F = @(x, y) (3);
R = func2str(F);
REF = '@(x,y)3';
assert_isequal(R, REF);
%=============================================================================
F = @(x) cos(x);
R = func2str(F);
REF = '@(x)cos(x)';
assert_isequal(R, REF);
%=============================================================================
F = @(x) cos(1) + cos(3);
R = func2str(F);
REF = '@(x)cos(1)+cos(3)';
assert_isequal(R, REF);
%=============================================================================
F = @(x) 1 + 3;
R = func2str(F);
REF = '@(x)1+3';
assert_isequal(R, REF);
%=============================================================================
F = @(x)1:3;
R = func2str(F);
REF = '@(x)1:3';
assert_isequal(R, REF);
%=============================================================================
F = @(x)[1:3];
R = func2str(F);
REF = '@(x)[1:3]';
assert_isequal(R, REF);
%=============================================================================
F = @(x)[1:cos(x)];
R = func2str(F);
REF = '@(x)[1:cos(x)]';
assert_isequal(R, REF);
%=============================================================================
F = @(x)cos(x):2;
R = func2str(F);
REF = '@(x)cos(x):2';
assert_isequal(R, REF);
%=============================================================================
F = @(x)1:cos(x);
R = func2str(F);
REF = '@(x)1:cos(x)';
assert_isequal(R, REF);
%=============================================================================
F = @(x)cos(sin(x));
R = func2str(F);
REF = '@(x)cos(sin(x))';
assert_isequal(R, REF);
%=============================================================================
F = @(x)1:3:4;
R = func2str(F);
REF = '@(x)1:3:4';
assert_isequal(R, REF);
%=============================================================================
F = @(x)1:2:3:4:4:5;
R = func2str(F);
REF = '@(x)1:2:3:4:4:5';
assert_isequal(R, REF);
%=============================================================================
F = @(x,y) ndgrid((-x:x/c:x),(-y:y/c:y));
R = func2str(F);
REF = '@(x,y)ndgrid(-x:x/c:x,-y:y/c:y)';
assert_isequal(R, REF);
%=============================================================================
