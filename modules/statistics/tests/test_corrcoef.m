assert_isequal(nargin('corrcoef'), 1);
assert_isequal(nargout('corrcoef'), 1);
%=============================================================================
R = corrcoef([]);
assert_istrue(isnan(R));
%=============================================================================
R = corrcoef(zeros(0, 3));
REF = zeros(3, 3) * NaN;
assert_isequal(R, REF);
%=============================================================================
R = corrcoef(1);
REF = NaN;
assert_isequal(R, REF);
%=============================================================================
R = corrcoef([1, 2, 3]);
REF = 1;
assert_isequal(R, REF);
%=============================================================================
assert_checkerror('R = corrcoef(ones(3,4,3));', _('Inputs must be 2-D.'));
